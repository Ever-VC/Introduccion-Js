// Arrow functions
const surmar2 = function(n1, n2) {
    console.log(n1 + n2);
}

const sumar = (n1, n2) => {
    console.log(n1 + n2);
}

const restar = (n1, n2) => console.log(n1 - n2);

const aprendiendo = tecnologia => console.log(`Aprendiendo ${tecnologia}`);

surmar2(2, 4);
sumar(2, 4);
restar(12, 10);
aprendiendo('JavaScript');